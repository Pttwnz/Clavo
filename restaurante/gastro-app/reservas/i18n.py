STRINGS: dict[str, dict[str, str]] = {
    "ca": _CA,
    "va": _VA,
    "es": _ES,
    "en": _EN,
}


def get_locale() -> str:
    lang = session.get("lang")
    if lang in SUPPORTED_LANGS:
        return lang
    return DEFAULT_LANG


def html_lang_attr() -> str:
    """Atribut BCP 47 per a <html lang>."""
    loc = get_locale()
    if loc == "en":
        return "en"
    if loc == "es":
        return "es"
    if loc == "va":
        return "ca-ES-valencia"
    return "ca"


def translate(key: str, **kwargs: object) -> str:
    loc = get_locale()
    # Reserva: locale actual → castellà (principal) → català → anglès
    chain = (loc, DEFAULT_LANG, "ca", "en")
    msg: str | None = None
    for code in chain:
        table = STRINGS.get(code)
        if table and key in table:
            msg = table[key]
            break
    if msg is None:
        msg = key
    if kwargs:
        try:
            return msg.format(**{k: v for k, v in kwargs.items() if v is not None})
        except (KeyError, ValueError):
            return msg
    return msg


def init_i18n(app) -> None:
    """Registra `tr`, `current_lang` i `html_lang` a les plantilles."""

    @app.context_processor
    def inject_i18n():
        return {
            "tr": translate,
            "current_lang": get_locale(),
            "html_lang": html_lang_attr,
            "supported_langs": SUPPORTED_LANGS,
        }
